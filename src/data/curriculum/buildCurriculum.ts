export function buildCurriculum(mode: 'anak' | 'dewasa'): Lesson[] {
  return SEEDS.map((seed) => ({
    id: seed.id,
    order: seed.order,
    title: seed.title,
    description: seed.description,
    keysIntroduced: seed.keysIntroduced,
    targetText: mode === 'anak' ? seed.textAnak : seed.textDewasa,
    minWpm: mode === 'anak' ? seed.minWpmAnak : seed.minWpmDewasa,
    minAccuracy: 90,
  }))
}
